package lista_05_03_2018;

public class Lance {

    private String nome;
    private double valorDoLance;

    public Lance(String nome, double valorDoLance) {
        this.nome = nome;
        this.valorDoLance = valorDoLance;
    }

    public String getNome() {
        return nome;
    }

    public double getValorDoLance() {
        return valorDoLance;
    }
}
